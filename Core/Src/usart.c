uint8_t MX_USART2_GetRxBusyFlag(void)
{
	if(UART2_RxBusyFlag)
	{
		if(UART2_RxBusyTick-- == 0)
		{
			UART2_RxBusyFlag = 0;
		}
	}

	return UART2_RxBusyFlag;
}


void HAL_UART_RxCpltCallback(UART_HandleTypeDef *uartHandle)
{
	if(uartHandle->Instance==USART1)
	{
		HAL_UART_Receive_IT(uartHandle, &rx_data1, 1);
		Que_PutByte(&uart1_rxQue,rx_data1);
	}
	else if(uartHandle->Instance==USART2)
	{
		HAL_UART_Receive_IT(uartHandle, &rx_data2, 1);
		Que_PutByte(&uart2_rxQue,rx_data2);
		UART2_RxBusyTick = UART2_RX_BUSY_TICK;
		UART2_RxBusyFlag = 1;
	}
	else if(uartHandle->Instance==UART4)
	{
		HAL_UART_Receive_IT(uartHandle, &rx_data4, 1);
		Que_PutByte(&uart4_rxQue,rx_data4);
	}
}
